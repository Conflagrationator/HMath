-- | The Description of the Structure of Mathematical Data
----------------------------------------------------------------

{-# LANGUAGE ExistentialQuantification #-}

module Structure where

----------------------------------------------------------------

import Unit

----------------------------------------------------------------
-- EXPRESSION
----------------------------------------------------------------

class Expression e where
    evaluate :: (Structure s) => e -> Either e s
    display :: e -> String

----------------------------------------------------------------



----------------------------------------------------------------
-- STRUCTURE
----------------------------------------------------------------

class (Eq s, Show s) => Structure s

----------------------------------------------------------------
-- OPERATOR
----------------------------------------------------------------

data Add a b = (Expression a, Expression b) => Add a b
